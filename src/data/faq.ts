// 동적 문자열 치환 헬퍼 함수
function replaceRegion(text: string, region: string): string {
  if (!text) return text;
  const r = region || "부산·경남·울산";
  let result = text.replace(/\{region\}/g, r);
  result = result.replace(/김해시/g, r);
  result = result.replace(/김해(?!시)/g, r);
  return result;
}

/**
 * 동적 키워드 유무 및 serviceGroup 에 맞게 전체 FAQ 목록을 리턴하는 헬퍼 함수
 */
export function getDynamicFaqs(keywordConfig: any): FaqItem[] {
  // 메인 페이지 (비활성화 상태) 전용 6가지 FAQ 대응
  if (!keywordConfig || !keywordConfig.isActive) {
    return mainPageFaqs;
  }

  const service = keywordConfig.service || "";
  const region = keywordConfig.region || "";

  let targetFaqs = waterproofingFaqs; // 기본값

  if (service === "외벽방수") {
    targetFaqs = exteriorWaterproofingFaqs;
  } else if (service === "외벽발수") {
    targetFaqs = exteriorWaterRepellentFaqs;
  } else if (service === "옥상방수") {
    targetFaqs = rooftopWaterproofingFaqs;
  } else if (service === "지붕방수") {
    targetFaqs = roofWaterproofingFaqs;
  } else if (service === "외벽도색") {
    targetFaqs = exteriorPaintingFaqs;
  } else if (service === "옥상누수") {
    targetFaqs = rooftopLeakFaqs;
  } else if (service === "외벽누수") {
    targetFaqs = exteriorLeakFaqs;
  } else if (service === "건물방수") {
    targetFaqs = buildingWaterproofingFaqs;
  } else {
    // 기존 로직 유지
    if (keywordConfig.serviceGroup === "leak-diagnosis") {
      targetFaqs = leakDiagnosisFaqs;
    } else if (keywordConfig.serviceGroup === "surface-protection") {
      targetFaqs = surfaceProtectionFaqs;
    }
  }

  // 지역명 치환 처리
  const regionName = region;
  
  const mappedFaqs = targetFaqs.map((faq) => ({
    id: faq.id,
    question: replaceRegion(faq.question, regionName),
    answer: replaceRegion(faq.answer, regionName)
  }));

  // 첫 번째 FAQ를 동적 키워드별 첫 질문/답변으로 치환하여 화면 및 JSON-LD 일치화
  // 단, 이번 단계에서 수정한 8개 서비스는 직접 선언한 Q1이 최우선이므로 치환하지 않음!
  const isCustomFaqService = ["외벽방수", "외벽발수", "옥상방수", "지붕방수", "외벽도색", "옥상누수", "외벽누수", "건물방수"].includes(service);
  if (!isCustomFaqService && keywordConfig.faqQuestion && keywordConfig.faqAnswer) {
    mappedFaqs[0] = {
      id: 1,
      question: replaceRegion(keywordConfig.faqQuestion, regionName),
      answer: replaceRegion(keywordConfig.faqAnswer, regionName)
    };
  }

  return mappedFaqs;
}

